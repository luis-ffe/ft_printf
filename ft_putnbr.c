#include "ft_printf.h"

int	ft_putnbr_fd(int n, int fd)
{
	char	*s;
	int		x;

	x = 0;
	s = ft_itoa(n);
	x = ft_putstr_fd(s, fd);
	free(s);
	return (x);
}
